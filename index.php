<h2>Bug Reports:</h2>

<p>If you run this code and encounter problems, you must report the bug <b>BY EMAIL</b> to
<a href="mailto:bugs@ircd-hybrid.org">bugs@ircd-hybrid.org</a>.&nbsp; Your
bug does not officically exist until it is reported to <a
href="mailto:bugs@ircd-hybrid.org">bugs@ircd-hybrid.org</a>. </p>

