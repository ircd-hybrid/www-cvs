<h2>Discussion:</h2>

<p>There is a mailing list for discussion of hybrid issues, including betas.&nbsp; To
subscribe, follow this link: <a href="https://lists.ircd-hybrid.org/mailman/listinfo/hybrid">https://lists.ircd-hybrid.org/mailman/listinfo/hybrid</a>.  This is the proper place to discuss new features, bugs, etc.&nbsp; Posting here is
